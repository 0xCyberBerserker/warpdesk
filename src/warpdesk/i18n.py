class I18N:
    def __init__(self) -> None:
        env_locale = ""
        for key in ("LC_ALL", "LC_MESSAGES", "LANG"):
            value = os.environ.get(key, "")
            normalized = value.split(".", 1)[0].upper()
            if value and normalized not in {"C", "POSIX"}:
                env_locale = value
                break
        locale_name = env_locale or QLocale.system().name()
        locale = locale_name.split(".", 1)[0].split("_", 1)[0].lower()
        self.language = locale if locale in MESSAGES else "en"

    def t(self, key: str, **kwargs: object) -> str:
        value = MESSAGES.get(self.language, MESSAGES["en"]).get(key, MESSAGES["en"].get(key, key))
        return value.format(**kwargs)
